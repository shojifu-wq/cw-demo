function generateDemoData(rooms) {
  const result = {};
  for (const room of rooms) {
    const content = DEMO_CONTENT[room.archetype];
    if (content) {
      result[room.id] = {
        messages: content.messages.map(m => ({ ...m })),
        tasks: content.tasks.map(t => ({
          ...t,
          limit: t.limitDays !== undefined
            ? Math.floor(Date.now() / 1000) + t.limitDays * 86400
            : undefined,
        })),
      };
    } else {
      result[room.id] = { messages: [], tasks: [] };
    }
  }
  return result;
}
